// slug.ts — URL/파일명용 slug 정규화 (순수 함수, 프론트·백엔드 공용).
//
// slug 는 URL 경로이자 _posts 파일명의 일부라 안전한 문자만 허용해야 한다.
// 사용자가 공백/대문자/한글/특수문자를 넣어도 항상 안전한 형태로 만든다.
//
// 규칙:
//   - 소문자화
//   - 공백·밑줄 → 하이픈
//   - 영문 소문자·숫자·하이픈 외 문자는 제거(한글·특수문자 등)
//   - 연속 하이픈은 하나로
//   - 앞뒤 하이픈 제거
//
// 한글은 제거한다(URL 은 영문 원칙). 한글 제목은 slug 제안(LLM)으로 영문 slug 를 만든다.
// 결과가 빈 문자열이면 호출측이 slug 제안을 유도하거나 발행을 막는다.

export function slugify(raw: string): string {
  return String(raw ?? '')
    .toLowerCase()
    .replace(/[\s_]+/g, '-') // 공백·밑줄 → 하이픈
    .replace(/[^a-z0-9-]/g, '') // 허용 문자(영소문자·숫자·하이픈)만 남김
    .replace(/-+/g, '-') // 연속 하이픈 → 하나
    .replace(/^-+|-+$/g, '') // 앞뒤 하이픈 제거
}
